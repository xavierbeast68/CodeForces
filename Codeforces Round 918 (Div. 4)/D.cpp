void solve(){
    cin >> n; cin >> s;
    map<char,char> mp{{'b', 'C'}, {'c', 'C'}, {'d', 'C'}, {'a', 'V'}, {'e', 'V'}};
    
    string ans = "";
    
    for(int i = 0; i < n;){
        
        if((i+3 < n && mp[s[i+3]] == 'C') || (i+3 == n)){
            
            ans += s.substr(i, 3);
            
            if(i+3 < n){
                ans += '.';
            }
            
            i = i+3;
        }
        else{
            ans += s.substr(i, 2);
            
            if(i+2 < n){
                ans += '.';
            }
            
            i = i +2;
        }
    }
    
    cout << ans << endl;
}

